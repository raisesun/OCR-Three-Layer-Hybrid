# ========== 构建函数 ==========

def build_prompt(doc_type_key: str, key_list: Optional[List[str]] = None) -> str:
    """根据文档类型构建完整 prompt（template + 公共后缀）

    Args:
        doc_type_key: DocumentType.value 字符串（如 "HOUSEHOLD_REGISTER"）
        key_list: 备选字段列表（当无专用模板时用于生成通用 prompt）

    Returns:
        完整的 prompt 字符串（template + COMMON_SUFFIX）
    """
    template = PROMPT_TEMPLATES.get(doc_type_key)
    if template is not None:
        return template + "\n\n" + COMMON_SUFFIX

    # 无专用模板时，使用 key_list 生成通用 prompt
    if key_list:
        keys_str = "、".join(key_list)
        return (
            f"请从图片中提取以下字段，以JSON格式返回，不要包含markdown标记：{keys_str}\n"
            "不存在的字段返回空字符串。\n\n"
        ) + COMMON_SUFFIX

    return (
        "请从图片中提取所有可识别的信息，以JSON格式返回。\n\n"
    ) + COMMON_SUFFIX
